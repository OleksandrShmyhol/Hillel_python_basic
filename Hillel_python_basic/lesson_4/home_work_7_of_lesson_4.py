# [0, 1, 0, 3, 12] -> [1, 3, 12, 0, 0]
#
# [0] -> [0]
#
# [1, 0, 3, 0, 0, 0, 5] -> [1, 3, 5, 0, 0, 0, 0]
#
# [9, 0, 7, 31, 0, 45, 0, 45, 0, 45, 0, 0, 96, 0] -> [9, 7, 31, 45, 45, 45, 96, 0, 0, 0, 0, 0, 0, 0]

lst = [9, 0, 7, 31, 0, 45, 0, 45, 0, 45, 0, 0, 96, 0]

for el in lst:
    if not el:
        lst.remove(el)
        lst.append(0)
    el += 1
print(lst)
